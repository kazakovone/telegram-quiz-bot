async def answer_handler(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    data = query.data
    if not data.startswith("ans_"):
        return

    await query.answer()

    try:
        choice = int(data.split("_")[1])
    except ValueError:
        return

    answers = context.user_data.get("answers", [])
    answers.append(choice)
    context.user_data["answers"] = answers

    q_index = context.user_data.get("q_index", 0) + 1
    context.user_data["q_index"] = q_index

    if q_index < len(QUESTIONS):
        await edit_question_message(query.message, q_index)
    else:
        # —Å—á–∏—Ç–∞–µ–º —Ç–∏–ø—ã
        counts = Counter(answers)
        for t in range(1, 6):
            counts.setdefault(t, 0)
        max_count = max(counts.values())
        dominant = [t for t, c in counts.items() if c == max_count]
        context.user_data["dominant_types"] = dominant

        text = (
            "–£—Ä–∞! üéâ –¢—ã –ø—Ä–æ—à—ë–ª(–∞) —Ç–µ—Å—Ç, –∏ –º—ã —Å–º–æ–≥–ª–∏ –æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å —Ç–≤–æ–π —Ç–∏–ø —Ñ–æ—Ç–æ–≥—Ä–∞—Ñ–∞.\n\n"
            "–ù–∞–∂–º–∏ –∫–Ω–æ–ø–∫—É –Ω–∏–∂–µ, —á—Ç–æ–±—ã —É–∑–Ω–∞—Ç—å —Å–≤–æ–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç."
        )
        await query.message.edit_text(text=text, reply_markup=build_after_quiz_keyboard())


