async def edit_question_message(query_message, q_index: int):
    """–û–±–Ω–æ–≤–ª—è–µ–º —Ç–µ–∫—É—â–µ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –Ω–æ–≤—ã–º –≤–æ–ø—Ä–æ—Å–æ–º."""
    q = QUESTIONS[q_index]
    keyboard = build_question_keyboard(q["options"])
    text = f"<b>{q['title']}</b>\n\n{q['question']}"

    await query_message.edit_text(
        text=text,
        parse_mode=ParseMode.HTML,
        reply_markup=keyboard,
    )


