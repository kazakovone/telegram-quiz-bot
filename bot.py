async def send_question(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """–û—Ç–ø—Ä–∞–≤–∫–∞ —Ç–µ–∫—É—â–µ–≥–æ –≤–æ–ø—Ä–æ—Å–∞ –ø–æ –Ω–æ–º–µ—Ä—É –≤ user_data['q_index']."""
    q_index = context.user_data.get("q_index", 0)
    if q_index >= len(QUESTIONS):
        return

    q = QUESTIONS[q_index]

    # –ø–æ–ª–Ω—ã–π —Ç–µ–∫—Å—Ç –≤–∞—Ä–∏–∞–Ω—Ç–æ–≤ –≤ —Å–∞–º–æ–º —Å–æ–æ–±—â–µ–Ω–∏–∏
    options_lines = [f"{i + 1}. {opt}" for i, opt in enumerate(q["options"])]
    text = (
        f"<b>{q['title']}</b>\n\n"
        f"{q['question']}\n\n" +
        "\n".join(options_lines)
    )

    keyboard = build_question_keyboard(q["options"])

    if update.callback_query:
        await update.callback_query.edit_message_text(
            text=text,
            reply_markup=keyboard,
            parse_mode=ParseMode.HTML,
        )
    else:
        await update.message.reply_text(
            text=text,
            reply_markup=keyboard,
            parse_mode=ParseMode.HTML,
        )


