def main():
    token = os.getenv("BOT_TOKEN")
    if not token:
        raise RuntimeError("–ù–µ –Ω–∞–π–¥–µ–Ω —Ç–æ–∫–µ–Ω. –£—Å—Ç–∞–Ω–æ–≤–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é BOT_TOKEN.")

